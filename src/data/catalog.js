const toTitleCase = (str) => {
  const minor = new Set(['a','al','con','de','del','el','en','la','las',
    'les','lo','los','para','por','sin','un','una','y','o','e','u']);
  return str
    .toLowerCase()
    .split(' ')
    .map((w, i) => i === 0 || !minor.has(w)
      ? w.charAt(0).toUpperCase() + w.slice(1)
      : w)
    .join(' ');
};

